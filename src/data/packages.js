export function getPackageByRoute(route) {
  const cleanRoute = route.toLowerCase().replace(/\/$/, "");
  return allPackages.find(p => p.route.toLowerCase().replace(/\/$/, "") === cleanRoute);
}

export function getPackageByName(name) {
  const cleanName = name.toLowerCase();
  return allPackages.find(p => p.name.toLowerCase() === cleanName);
}
